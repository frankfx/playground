package dbs1.persistence.data;

public class ElektroMaschine extends AbstrakteMaschine{
	
	protected int netzspannung;
	
	protected int aufnahmeleistung;

	public ElektroMaschine() {
		super();
	}

	public int getNetzspannung() {
		return netzspannung;
	}

	public void setNetzspannung(int netzspannung) {
		this.netzspannung = netzspannung;
	}

	public int getAufnahmeleistung() {
		return aufnahmeleistung;
	}

	public void setAufnahmeleistung(int aufnahmeleistung) {
		this.aufnahmeleistung = aufnahmeleistung;
	} 
	
	
}

package dbs1.persistence.data;

public class PMZuteilung{
	
	protected long id; 
	
	protected int note;
	
	protected AbstrakteMaschine maschine;
	
	protected Person mitarbeiter;

	public PMZuteilung() {
		super();
	}

	
	
	public PMZuteilung(int note, AbstrakteMaschine maschine, Person mitarbeiter) {
		super();
		this.note = note;
		this.maschine = maschine;
		this.mitarbeiter = mitarbeiter;
	}



	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getNote() {
		return note;
	}

	public void setNote(int note) {
		this.note = note;
	}

	public AbstrakteMaschine getMaschine() {
		return maschine;
	}

	public void setMaschine(AbstrakteMaschine maschine) {
		this.maschine = maschine;
	}

	public Person getMitarbeiter() {
		return mitarbeiter;
	}

	public void setMitarbeiter(Person mitarbeiter) {
		this.mitarbeiter = mitarbeiter;
	}
	
	
	
}
